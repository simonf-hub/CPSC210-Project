package model;

public class EfficientBlock {
    private int theX;
    private int theY;

    public EfficientBlock(int x, int y) {
        theX = x;
        theY = y;
    }



    //getters
    public int getTheX() {
        return theX;
    }

    public int getTheY() {
        return theY;
    }
}
